function loadQuestions(examType) {
    const questionnaire = document.getElementById('questionnaire');
    const questions = questionsData[examType];
    questionnaire.innerHTML = `<h1>${examType} Questions</h1>`;
    questions.forEach((q, index) => {
        questionnaire.innerHTML += `
            <div class="question">
                <h3>Q${index + 1}: ${q.question}</h3>
                <div class="options">
                    ${q.options.map(option => `
                        <label>
                            <input type="radio" name="q${index}" value="${option}"> ${option}
                        </label>
                    `).join('')}
                </div>
            </div>
        `;
    });
    questionnaire.innerHTML += `<button onclick="submitAnswers('${examType}')">Submit Answers</button>`;
}

function submitAnswers(examType) {
    const questions = questionsData[examType];
    let score = 0;
    questions.forEach((q, index) => {
        const userAnswer = document.querySelector(`input[name="q${index}"]:checked`);
        if (userAnswer && userAnswer.value === q.answer) {
            score++;
        }
    });
    alert(`You scored ${score} out of ${questions.length}!`);
}


